// sub topic kedua Tipe Data Numbers
// numbers dalam javascript adalah tipe data yang digunakan untuk menyimpan nilai numerik
// didalam javascript terdapat dua tipe data numbers yaitu integer dan float
// integer adalah bilangan bulat sedangkan float adalah bilangan desimal
// memiliki bilangan negatif dan positif
// contoh penggunaan bilangan negatif dan positif
let positif = 10;
let negatif = -10;
console.log(positif);
console.log(negatif);
// contoh penggunaan numbers
let integer = 10;
let float = 10.5;
console.log(integer);
console.log(float);
