// method push dan pop pada arrray
// method push digunakan untuk menambahkan nilai baru pada array
// method pop digunakan untuk menghapus nilai terakhir pada array
//penggunaan method push
var buah = ["apel", "mangga", "jeruk"];
console.log(buah); //output: ['apel', 'mangga', 'jeruk']
buah.push("semangka");
console.log(buah); //output: ['apel', 'mangga', 'jeruk', 'semangka']

//penggunaan method pop
buah.pop();
console.log(buah); //output: ['apel', 'mangga', 'jeruk']
